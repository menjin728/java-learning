package src.menjinyan.java;

public class Demo04ZiZenZiJian {
    public static void main(String[] args) {
        //自增自减运算符
        int x=10;
        System.out.println( "x:"+x);
        int y=x++;
        System.out.println( "y:"+y);
        int z=++x;
        System.out.println( "z:"+z);
    }
}
